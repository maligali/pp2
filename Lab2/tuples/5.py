#Loop tuple

#Loop Through a Tuple
tup = ("juice", "cola", "sprite", "fanta")
for x in tup:
    print(x)

#Loop Through the Index Numbers
tup = ("juice", "cola", "sprite", "fanta")
for i in range(len(tup)):
  print(tup[i])

#Using a While Loop
tup = ("juice", "cola", "sprite", "fanta")
i = 0
while i < len(tup):
  print(tup[i])
  i = i + 1

  