#List

mylist = ["cookie", "strawberry", "cake"]
print(mylist)

'''List Items
List items are ordered, changeable, and allow duplicate values.

List items are indexed, the first item has index [0], the second item has index [1] etc.'''

mylist = ["cookie", "cola", "cookie"]
print(mylist)

#List length

mylist = ["lemon", "mango", "cherry"]
print(len(mylist))

#List items can be of any data type
list1 = ["cake", "cookie", "cherry"]
list2 = [7, 2, 18, 22]
list3 = [True, False, True]

list1 = [55, "happy", 55, False, "make"]

#type

mylist = ["horse", "elephant", "cat"]
print(type(mylist)) #<class 'list'>

#list() constructor
mylist = list(("cow", "dog", "rabbit"))
print(mylist)