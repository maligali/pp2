#Upper case
a = "I eat vegetables!"
print(a.upper())

#Lower case
a = "I do not like fruits!"
print(a.lower())

#Remove Whitespace
a = " Eat healthy! "
print(a.strip()) # returns "Eat healthy!"

#Replace String
a = "I am eating!"
print(a.replace("I", "M"))

#Split string
a = "I love you, food!"
print(a.split(",")) # returns ['I love you', ' food!']